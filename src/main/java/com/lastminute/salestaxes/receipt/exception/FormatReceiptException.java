package com.lastminute.salestaxes.receipt.exception;

/**
 * The Class FormatReceiptException is created to handle Exceptions in creating
 * the format of the Receipt.
 */
public class FormatReceiptException extends Exception {

	private static final long serialVersionUID = 1L;

	public FormatReceiptException() {
		super();
	}

	public FormatReceiptException(Throwable message) {
		super(message);
	}

}
