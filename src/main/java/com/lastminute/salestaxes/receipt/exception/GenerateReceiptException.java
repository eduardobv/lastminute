package com.lastminute.salestaxes.receipt.exception;

/**
 * The Class GenerateReceiptException is created to handle Exceptions in
 * generating the Receipt.
 */
public class GenerateReceiptException extends Exception {

	private static final long serialVersionUID = 1L;

	public GenerateReceiptException() {
		super();
	}

	public GenerateReceiptException(Throwable message) {
		super(message);
	}

}
